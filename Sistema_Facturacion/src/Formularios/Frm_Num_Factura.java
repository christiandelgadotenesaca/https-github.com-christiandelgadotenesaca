    private String capturarfechaInicio(){
        int dia = jDateInicio.getCalendar().get(Calendar.DAY_OF_MONTH);
        int mes = jDateInicio.getCalendar().get(Calendar.MONTH)+1;
        int año = jDateInicio.getCalendar().get(Calendar.YEAR) ;
        String FechaInicio = String.valueOf(año)+"-"+String.valueOf(mes)+"-"+String.valueOf(dia);
        return FechaInicio;
    }
    private String capturarfechaFin(){
        int dia = jDateFin.getCalendar().get(Calendar.DAY_OF_MONTH);
        int mes = jDateFin.getCalendar().get(Calendar.MONTH)+1;
        int año = jDateFin.getCalendar().get(Calendar.YEAR) ;
        String FechaFin = String.valueOf(año)+"-"+String.valueOf(mes)+"-"+String.valueOf(dia);
        return FechaFin;
    }
