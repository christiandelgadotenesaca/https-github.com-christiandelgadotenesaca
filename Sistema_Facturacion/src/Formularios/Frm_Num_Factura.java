    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        if ((Integer.parseInt(txt_num_siguiente.getText()) >= Integer.parseInt(txt_inicio.getText())) && 
           (Integer.parseInt(txt_num_siguiente.getText())<=Integer.parseInt(txt_fin.getText()))){       
        String fechainicio = capturarfechaInicio();
        String fechafin = capturarfechaFin();
        Cls_Transacciones a = new Cls_Transacciones(txtcodigo.getText(),txt_descripcion.getText(),Integer.parseInt(txt_inicio.getText()),Integer.parseInt(txt_fin.getText()),Integer.parseInt(txt_num_siguiente.getText()) , fechainicio, fechafin);
        a.Guardar();
        a.CargarDatos(tblTransacciones);
        JOptionPane.showMessageDialog(null, "Se Guardo satisfactoriamente");
        limpieza();
        }
        else{
         JOptionPane.showMessageDialog(null, "Error en el numero proximo para la factura","Error",JOptionPane.ERROR_MESSAGE);
        }    
    }//GEN-LAST:event_btn_guardarActionPerformed
    private void limpieza(){
        txtcodigo.setText("");
        txt_descripcion.setText("");
        txt_inicio.setText("");
        txt_fin.setText("");
        txt_num_siguiente.setText("");
    }
    private void tblTransaccionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTransaccionesMouseClicked
        // TODO add your handling code here:
        int valor =  showConfirmDialog(null,"¿Seleccionar la transacción?","OPCION", JOptionPane.YES_NO_OPTION);
        if(valor==0){
            if(tblTransacciones.getSelectedRow()>=0)
            {
               String [] datosLeidos={
                    String.valueOf(tblTransacciones.getValueAt(tblTransacciones.getSelectedRow(),0)),
                    String.valueOf(tblTransacciones.getValueAt(tblTransacciones.getSelectedRow(),1)),
                    String.valueOf(tblTransacciones.getValueAt(tblTransacciones.getSelectedRow(),2)),
                    String.valueOf(tblTransacciones.getValueAt(tblTransacciones.getSelectedRow(),3)),
                    String.valueOf(tblTransacciones.getValueAt(tblTransacciones.getSelectedRow(),4)),
                    String.valueOf(tblTransacciones.getValueAt(tblTransacciones.getSelectedRow(),5)),
                    String.valueOf(tblTransacciones.getValueAt(tblTransacciones.getSelectedRow(),6)),
                    };
            txtcodigo.setText(datosLeidos[0]);
            txt_descripcion.setText(datosLeidos[1]);
            //Pasar la fecha a el jDate Inicio
            String fechaIni = datosLeidos[2].trim();
            String AñoIni = fechaIni.substring(0,4);
            String MesIni = fechaIni.substring(5,7);
            String DiaIni = fechaIni.substring(8,10);
            Calendar c2 = Calendar.getInstance();
            c2.set(Integer.parseInt(AñoIni),Integer.parseInt(MesIni)-1,Integer.parseInt(DiaIni));
            jDateInicio.setCalendar(c2);
            //Pasar la fecha a el jDate Inicio
            String fechaFin = datosLeidos[3].trim();
            String AñoFin = fechaFin.substring(0,4);
            String MesFin = fechaFin.substring(5,7);
            String DiaFin = fechaFin.substring(8,10);
            Calendar c3 = Calendar.getInstance();
            c3.set(Integer.parseInt(AñoFin),Integer.parseInt(MesFin)-1,Integer.parseInt(DiaFin));
            jDateFin.setCalendar(c3); 
            
             txt_inicio.setText(datosLeidos[4]);
             txt_fin.setText(datosLeidos[5]);
             txt_num_siguiente.setText(datosLeidos[6]);
            }
        }
    }//GEN-LAST:event_tblTransaccionesMouseClicked

